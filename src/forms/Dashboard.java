    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (cid == -1) {
            JOptionPane.showMessageDialog(rootPane, "First Select Item!");
        } else {
            int statu = JOptionPane.showConfirmDialog(rootPane, "Are you delete item!", "Message", JOptionPane.YES_NO_OPTION);
            if (statu == 0) {
                DB db = new DB();
                int deleteStatu = db.customerDelete(cid);
                if (deleteStatu > 0) {
                    customerTableRefresh();
                    cid = -1;
                }
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (cid == -1) {
            JOptionPane.showMessageDialog(rootPane, "First Select Item!");
        } else {
            int row = tblCustomer.getSelectedRow();
            UserUpdate.cid = this.cid;
            UserUpdate.name = "" + tblCustomer.getValueAt(row, 1);
            UserUpdate.mail = "" + tblCustomer.getValueAt(row, 2);
            UserUpdate.tel = "" + tblCustomer.getValueAt(row, 3);
            UserUpdate.adr = "" + tblCustomer.getValueAt(row, 4);
            
            UserUpdate upt = new UserUpdate();
            upt.setVisible(true);
            dispose();
            
        }


    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        
        if (cid == -1) {
            JOptionPane.showMessageDialog(rootPane, "First Select Item!");
        } else {
            
            String sizeString = JOptionPane.showInputDialog("Size place!");
            DB db = new DB();
            int statu = db.newOrder(cid, Integer.valueOf(sizeString));
            if ( statu > 0 ) {
                orderTableRefresh();
                cid = -1;
            }
            
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

