    public void login() {
        String userName = txtName.getText().trim();
        
        char[] arr = txtPass.getPassword();
        String pass = String.valueOf(arr).trim();
        
        if (userName.equals("")) {
            lblError.setText("UserName Not Empty !");
            txtName.requestFocus();
        }else if ( pass.equals("")) {
            lblError.setText("Password Not Empty !");
            txtPass.requestFocus();
        }else {
           if  (db.login(userName, pass).size() > 0 ) {
               // admin login - Redirect Dashboard
               Dashboard dh = new Dashboard();
               dh.setVisible(true);
               dispose(); // mevcut sınıfı kapat
           }else {
               lblError.setText("UserName or Password Fail!");
           }
        }
        
    }
    
    
